function cekStatus() {
  const nmrpanggilInput = document.getElementById('nmrpanggil').value;
  const statusDiv = document.getElementById('status');

  if (daftarBuku.hasOwnProperty(nmrpanggilInput)) {
    const statusBuku = daftarBuku[nmrpanggilInput];
    if (statusBuku === 'dipinjam') {
      statusDiv.textContent = 'Buku dengan Nomor Panggil ' + nmrpanggilInput + ' sedang dipinjam dan belum dikembalikan.';
    } else if (statusBuku === 'tersedia') {
        statusDiv.textContent = 'Buku dengan Nomor Panggil ' + nmrpanggilInput + ' tersedia.';
    } else {
      statusDiv.textContent = 'Status buku tidak valid.';
    }
  } else {
    statusDiv.textContent = 'Buku dengan Nomor Panggil ' + nmrpanggilInput + ' tidak ditemukan.';
  }
}